// Objetos globais pré-definidos
// Traz o nome da chave
console.log("keys", Object.keys({ nome: "Bruno" }));

// Traz o valor da chave
console.log("values", Object.values({ nome: "Bruno" }));

// Traz a data como milissegundo de hoje até 1970 quando linux foi lançado
console.log("now", Date.now());

// Função randomica
console.log("random", Math.random());

// Função global, onde mostra todas as funções que podem ser utilizadas
console.log("global", global);
